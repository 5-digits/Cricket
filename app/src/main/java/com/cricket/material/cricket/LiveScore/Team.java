
package com.cricket.material.cricket.LiveScore;

public class Team {

    private String i;
    private String fn;
    private String sn;
    private Logo logo;
    private Flag flag;





    public String getI() {
        return i;
    }




    public void setI(String i) {
        this.i = i;
    }




    public String getFn() {
        return fn;
    }




    public void setFn(String fn) {
        this.fn = fn;
    }




    public String getSn() {
        return sn;
    }




    public void setSn(String sn) {
        this.sn = sn;
    }




    public Logo getLogo() {
        return logo;
    }




    public void setLogo(Logo logo) {
        this.logo = logo;
    }




    public Flag getFlag() {
        return flag;
    }




    public void setFlag(Flag flag) {
        this.flag = flag;
    }



}
