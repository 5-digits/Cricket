
package com.cricket.material.cricket.LiveScore;

public class Toss {

    private String win;
    private String bat;
    private String text;





    public String getWin() {
        return win;
    }




    public void setWin(String win) {
        this.win = win;
    }




    public String getBat() {
        return bat;
    }




    public void setBat(String bat) {
        this.bat = bat;
    }




    public String getText() {
        return text;
    }




    public void setText(String text) {
        this.text = text;
    }



}
