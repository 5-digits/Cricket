
package com.cricket.material.cricket.LiveScore;

import java.util.ArrayList;
import java.util.List;


public class Scorecard {

    private String v;
    private String mid;
    private String m;
    private Series series;
    private String ecf;
    private Place place;
    private String mn;
    private List<Team> teams = new ArrayList<Team>();
    private PastIngs pastIngs;
    private Toss toss;
    private String result;
    private String ms;
    private String mt;
    private String cts;





    public String getV() {
        return v;
    }




    public void setV(String v) {
        this.v = v;
    }




    public String getMid() {
        return mid;
    }




    public void setMid(String mid) {
        this.mid = mid;
    }




    public String getM() {
        return m;
    }




    public void setM(String m) {
        this.m = m;
    }




    public Series getSeries() {
        return series;
    }




    public void setSeries(Series series) {
        this.series = series;
    }




    public String getEcf() {
        return ecf;
    }




    public void setEcf(String ecf) {
        this.ecf = ecf;
    }




    public Place getPlace() {
        return place;
    }




    public void setPlace(Place place) {
        this.place = place;
    }




    public String getMn() {
        return mn;
    }




    public void setMn(String mn) {
        this.mn = mn;
    }




    public List<Team> getTeams() {
        return teams;
    }




    public void setTeams(List<Team> teams) {
        this.teams = teams;
    }




    public PastIngs getPastIngs() {
        return pastIngs;
    }




    public void setPastIngs(PastIngs pastIngs) {
        this.pastIngs = pastIngs;
    }




    public Toss getToss() {
        return toss;
    }




    public void setToss(Toss toss) {
        this.toss = toss;
    }




    public String getResult() {
        return result;
    }




    public void setResult(String result) {
        this.result = result;
    }




    public String getMs() {
        return ms;
    }




    public void setMs(String ms) {
        this.ms = ms;
    }




    public String getMt() {
        return mt;
    }




    public void setMt(String mt) {
        this.mt = mt;
    }




    public String getCts() {
        return cts;
    }




    public void setCts(String cts) {
        this.cts = cts;
    }



}
